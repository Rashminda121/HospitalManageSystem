    private void Selectional(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalmanage","root","1234");
            st=conn.createStatement();
            
            String sql="select * from patient ";
            rs=st.executeQuery(sql);
            
            //doctorTable.setModel(DbUtils.resultSetToTableModel(rs));
            
            while(rs.next()){
                String id=String.valueOf(rs.getInt("patid"));
                String patName=String.valueOf(rs.getString("patname"));
                String patAdd=String.valueOf(rs.getString("patadd"));
                String patPho=String.valueOf(rs.getString("patphone"));
                String patAge=String.valueOf(rs.getString("patage"));
                String patGen=String.valueOf(rs.getString("patgender"));
                String patGrp=String.valueOf(rs.getString("patgrp"));
                String patPatho=String.valueOf(rs.getString("patpatho"));
                
               
                String tbData[]={id,patName,patAdd,patPho,patAge,patGen,patGrp,patPatho};
                DefaultTableModel tblModel=(DefaultTableModel)patTable.getModel();
                
                tblModel.addRow(tbData);
            }
            conn.close();
          
        }catch(Exception e){
           e.printStackTrace();
        }
    }
    
