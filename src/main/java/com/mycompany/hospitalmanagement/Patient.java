    private void ptAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ptAddMouseClicked
         
        if(patId.getText().isEmpty() ||patName.getText().isEmpty()||patAdd.getText().isEmpty()||patPhone.getText().isEmpty()||patAge.getText().isEmpty()
                || patGen.getSelectedItem().toString().isEmpty()||patGroup.getSelectedItem().toString().isEmpty()||patPatho.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Infromation");
        }else{
           try{
            conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/hospitalmanage","root","1234");
            
            String sql="insert into patient values(?,?,?,?,?,?,?,?)";
            
            PreparedStatement ps=conn.prepareStatement(sql);
            ps.setInt(1,Integer.valueOf(patId.getText()));
            ps.setString(2,patName.getText());
            ps.setString(3,patAdd.getText());
            ps.setString(4,patPhone.getText());
            ps.setInt(5,Integer.valueOf(patAge.getText()));
            ps.setString(6,patGen.getSelectedItem().toString());
            ps.setString(7,patGroup.getSelectedItem().toString());
            ps.setString(8,patPatho.getText());
            
            int row=ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Patient Added Succssfully.");
            
             
            //
            SelectionClear();
            Selectional();
        
            conn.close();
            patClear();
                    
                    
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Something Went Wrong.");
        }
        }
        
        
        
    }//GEN-LAST:event_ptAddMouseClicked

